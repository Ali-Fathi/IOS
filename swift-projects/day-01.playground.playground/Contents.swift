// Day 1 hackingwithswift

// Variables
var greeting = "Hello, playground"
print(greeting)
greeting = "Goodbye, playground!"
print(greeting)

// Strings and Integers
var age = 38
var population = 7_000_000
print(age)
print(population)

// Multi-line strings
var str = """
this goes
over multiple
lines
"""
print(str)
var str1 = """
this goes \
over multiple \
lines
"""
print(str1)

// Doubles and Booleans
var pi = 3.141
print(pi)
var awesome = true
print(awesome)

// string interpolation
var score = 85
var str2 = "Your Score was \(score)"
var result = "The test results are here: \(str2)"
print(result)

// Constants
let taylor = "swift"
print(taylor)

// Type annotation
let playground = "swift playground"
print(type(of: playground))
let album: String = "Reputation"
print(type(of: album))
let year: Int = 1989
print(type(of: year))
let height: Double = 1.78
print(type(of: height))
let taylorRocks: Bool = true

